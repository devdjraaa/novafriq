export const NOMS_ICONES = Object.keys(ICONES)

/**
 * Rend une icône par son nom.
 *
 * Un nom inconnu ne rend RIEN plutôt qu'un carré vide ou une erreur : une
 * icône absente doit se voir comme une absence, pas casser la page qui
 * l'entoure.
 */
export function Icone({ nom, taille = 24, ...reste }) {
  const tracé = ICONES[nom]
  if (!tracé) return null

  return (
    <svg
      width={taille}
      height={taille}
      viewBox="0 0 24 24"
      fill="none"
      stroke="currentColor"
      strokeWidth="1.6"
      strokeLinecap="round"
      strokeLinejoin="round"
      aria-hidden="true"
      {...reste}
    >
      {tracé}
    </svg>
  )
}
